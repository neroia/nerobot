function abri() {
    const lista = document.getElementById('beneficios');
    if (lista.style.display === 'none' || lista.style.display === '') {
        lista.style.display = 'block';  // Torna visível
    } else {
        lista.style.display = 'none';  // Torna invisível
    }
}

